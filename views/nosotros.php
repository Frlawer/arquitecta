            <h2>BORGO ARQUITECTURA / S&H</h2>
        </header>
        <p>Con sus sedes en San Carlos de Bariloche y Neuquen Capital, somos un estudio que cuenta con más de 20 años de trayectoria al servicio de la arquitectura.</p>
        <p>Conformado por un equipo profesional de gente seria, responsable, brindamos un servicio de calidad a nuestros clientes.</p>    
        <p>Nuestros diseños se realizan partiendo de un estudio pormenorizado de la región, a través del uso de materiales de la zona y teniendo en cuenta los factores climáticos, topográficos, siempre con la premisa de dar diseño y función.</p>
        <p>En la mayor parte de nuestra trayectoria, los diseños fueron de montaña, aplicando estandartes de Higiene y seguridad, cálculos estructurales acorde a la zona sismo resistentes y diagramas de evolución de obra, teniendo en cuenta le clima inestable de la zona.</p>
        <p>Con más de 20000 m2 realizados, entre ellas establecimientos turísticos, viviendas unifamiliares y obras institucionales, el estudio se ha afianzado en los armar proyectos, siempre teniendo en cuenta, en simultáneo, el trabajo con nuestros asesores en los diferentes servicios de infraestructura.</p>
        <p>La alta calidad de nuestra arquitectura nos ha permitido Trasladar nuestra trayectoria y experiencia a la ciudad de Neuquen y alrededores.</p>

        <h2>ARQ. JULIETA BORGONOVO</h2>
        <p>"Convencida desde muy temprana edad que el diseño y la construcción eran mi destino, desarrollo mi carrera con la certeza de que un buen diseño trasciende los límites de sus habitantes y hace de este mundo, un hermoso lugar."</p>
        <p>Nacida en la ciudad Rosario, y egresada como Arquitecta de la Universidad Nacional de Rosario, años más tarde obteniendo el Título de Posgrado en Higiene &amp; Seguridad en la Industria de la Construcción en la Universidad de La Plata. Vivo en la Patagonia argentina desde hace más de 30 años.</p>
        <p>Toda mi experiencia sumando la seriedad, el compromiso y el incondicional deseo de hacer un buen diseño, avalan mi historia profesional agregándole los innumerables cursos que he tomado, así como participaciones en congresos, concursos y jornadas profesionales a las que asisto permanentemente.</p>
